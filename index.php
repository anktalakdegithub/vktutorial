        dataType: "json",
        type: "post",
        success: function(data) {

            debugger;
            if (data.code == "404") {
                $('#msg').html('<div class="alert alert-danger mt-3" role="alert">' + data.msg +
                    '</div>');
            } else {
                $('#msg').html('<div class="alert alert-success mt-3" role="alert">' + data.msg +
                    '</div>');
                // purchase_product();
                // window.location.href="contact.php";					

            }

        }
    });
